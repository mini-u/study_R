## 3. 벡터의 이해
#벡터값 추출하기

d <- c(1,4,3,7,8)
names(d) <-c("첫번째 숫자", "두번째 숫자", "세번째 숫자","네번째 숫자","다섯번째 숫자")
d
d[1]
d[4] #네번째 값 추출
d[c(1,2,5)] #첫번째, 두번째, 세번째 값 추출
d[1:3] #첫번째부터 세번째까지 추출
d[seq(1,5,2)] #첫번째부터 다섯번째까지 2칸씩 띄어서 추출
d[-2] #2번째 값만 빼놓고 추출
d[-c(4:5)] #4번째부터 5번째까지 빼놓고 추출
d[6] #대응값이 없으므로 NA출력
d["세번째 숫자"] #이름으로 출력
d[c("세번째 숫자", "두번째 숫자")] #이름으로 출력2

